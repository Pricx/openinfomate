def t(lang: str, text: str) -> str:
    """
    Translate a UI string into the requested language.

    We intentionally keep English as the identity translation to reduce key management.
    """
    if normalize_lang(lang) != "zh":
        return text
    return ZH_TRANSLATIONS.get(text, text)
