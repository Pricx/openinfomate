LANG_COOKIE_NAME = "tracker_lang"

SUPPORTED_LANGS: list[dict[str, str]] = [
    {"code": "en", "label": "EN"},
    {"code": "zh", "label": "中文"},
]


def normalize_lang(value: str | None) -> str:
    raw = (value or "").strip().lower()
    if not raw:
        return "en"
    if raw in {"zh", "zh-cn", "zh-hans", "zh-hant", "cn"} or raw.startswith("zh"):
        return "zh"
    return "en"


def _accept_language_prefers_zh(header_value: str) -> bool:
    s = (header_value or "").strip().lower()
    if not s:
        return False
    # Examples:
    # - "zh-CN,zh;q=0.9,en;q=0.8"
    # - "en-US,en;q=0.9"
    parts = [p.strip() for p in s.split(",") if p.strip()]
    for part in parts:
        lang = part.split(";", 1)[0].strip()
        if lang.startswith("zh"):
            return True
    return False


def get_request_lang(request: Request) -> str:
    cookie = (request.cookies.get(LANG_COOKIE_NAME) or "").strip()
    if cookie:
        return normalize_lang(cookie)
    accept = (request.headers.get("accept-language") or "").strip()
    if _accept_language_prefers_zh(accept):
        return "zh"
    return "en"


